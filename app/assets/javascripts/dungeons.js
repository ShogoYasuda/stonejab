  function select_party(role) {
    $('.input-'+ role).keyup(function(e) {
      if (e.keyCode === 13) {
        if ($('.input-' + role).val() != "") {
          $('.select-'+ role +'-box').css('display', 'block');
          var get_id = [];
          var id = [];
          for(var i = 0, len = monsters.length; i < len; i++) {
            if(monsters[i].indexOf($('.input-'+ role).val()) != -1) {
              get_id.push(i);
            };
          };
          $('.select-'+ role +'-box').empty();
          for(var n = 0; n < get_id.length; n++) {
            $('.select-'+ role +'-box').append("<div class='select-"+ role+"' id='"+get_id[n]+"'>"+monsters[get_id[n]]+"</div>");
          };
          $(".select-"+ role).click(function() {
            $('.input-'+ role).val(monsters[$(this).attr('id')]);
            $('.select-'+ role +'-box').css('display', 'none');
            $('.selected-'+ role +'-image').remove();
            $('.selected-monster').append("<img src='/assets/monsters/"+renamed_monsters[get_index(monsters[$(this).attr('id')])]+"' class='selected-"+ role +"-image'>");
          });
        };
      };
    });
  };

